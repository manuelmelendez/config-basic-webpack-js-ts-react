/**
 * Esta funcion se encarga de la suma de 2 numeros
 * @param {Number} a Es el  primer numero para sumar
 * @param {Number} b Es el segundo numera para realizar la suma
 * @returns {Number} va a retornar el resultado de la suma
 */

const suma = (a, b) => {
    return a + b;
}

export default suma;